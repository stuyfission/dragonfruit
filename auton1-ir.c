void encoderClear(){
	nMotorEncoder[left] = 0;
	nMotorEncoder[right] = 0;
	nMotorEncoder[liftLbottom] = 0;
	nMotorEncoder[liftRbottom] = 0;
}

//Intellectual property of Alvin
void driveMotors (int encoderTicks, int speed) {
	encoderClear();
	while ((nMotorEncoder[left] < encoderTicks) && (nMotorEncoder[right] < encoderTicks)) {
		motor[left] = speed;
		motor[right] = speed;
	}
	motor[left] = 0;
	motor[right] = 0;
	encoderClear();
}

void quarterTurnClock(int speed){
	encoderClear();
	while ((nMotorEncoder[left] < 850) && (nMotorEncoder[right] < 850)){
		motor[left] = -speed;
		motor[right] = speed;
	}
	motor[left] = 0;
	motor[right] = 0;
	encoderClear();
}

void quarterTurnCounterClock(int speed){
	encoderClear();
	while ((nMotorEncoder[left] < 850) && (nMotorEncoder[right] < 850)){
		motor[left] = speed;
		motor[right] = -speed;
	}
	motor[left] = 0;
	motor[right] = 0;
	encoderClear();
}

void initServos(){
	servoChangeRate[auton] = 2;
	servo[auton] = 255;
	servoChangeRate[auton] = 10;
	servo[servoL] = 0;
	servo[servoR] = 255;
	servo[lockL] = 100;
	servo[lockR] = 155;
}

